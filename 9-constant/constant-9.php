    - Constant Adalah Tempat Untuk Menyimpan Data Yang Tidak Bisa Diubah Lagi Setelah Di Deklarasikan
    - Untuk Membuat Constant Kita Bisa Menggunakan Function define () => define ("AUTHOR", Programmer Zaman Now);
    - Best Practice Pembuatan Nama Constant Adalah Menggunakan UPPER_CASE / PAKAI HURUF KAPITAL SEMUA => define ("UMUR_AGUNG_SAPUTRO", 23);

    => Tidak Boleh Menggunakan Nama Constant Yang Sama, Karena Bisa Error

    => Bisa Digunakan Di Semua Tipe Data

    => define => Sebagai Function Constant

    => ("AUTHOR") => Sebagai Nama Constant

    => Programmer Zaman Now => Sebagai Value (Sesuai Isi Data Jika Karakter Pakai String, Kalau Angka Pakai Number)
