    Constant
    - Variable Di PHP Sifatnya Mutable, Artinya Bisa Dirubah => Isi Dari Variable Itu Bisa Dirubah - Ubah
    - Jika Kita Ingin Membuat Variable Yang Immutable (Tidak Bisa Dirubah), Maka Tidak Bisa Kita Lakukan Di PHP
