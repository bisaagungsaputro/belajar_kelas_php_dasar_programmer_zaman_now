// *** Setting PATH ***
/*
    Setting PATH
    - Setelah Menginstall PHP, Kita Perlu Mengatur PATH, Sistem Operasi Yang Kita Gunakan
    - Tiap Sistem Operasi Berbeda - Beda Cara Setting PATH Nya
    - Inti Dari PATH Adalah Agar Kita Bisa Mengakses PHP Dari Command Prompt / Terminal

    => PATH PHP Dengan Cara :
       C:\xampp\php  => Selanjutnya Kita Buka Environment (Edit The System Environment Variables) Pilih Menu Advanced Dan Klik Environment Variables, Selajutnya Pilih System Variables Dengan Klik Edit, Selanjutnya Tambahkan Dengan Klik New Lalu Isi C:\xampp\php
       Kalau Di Tambahkan Di User Hanya User Yang Bisa Di Pakai, Kalau Saya masukkan Di System Variables Berarti Semua User Dilaptop Windows Bisa Menggunakan PHP


    => Untuk Melihat Versi PHP Di Terminal Menggunakan :
       php --version

    => Kalian Bisa Melihat Output PHP Dari Terminal Jadi Tidak Pelu Lewat Web Server
*/

